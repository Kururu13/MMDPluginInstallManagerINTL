        #region LicenseDatas変更通知プロパティ

        private List<LicenseData> _LicenseDatas;

        public List<LicenseData> LicenseDatas
        {
            get { return _LicenseDatas; }
            set
            {
                if (_LicenseDatas == value)
                {
                    return;
                }
                _LicenseDatas = value;
                RaisePropertyChanged();
            }
        }

        private LicenseData _SelectedLicense;

        public LicenseData SelectedLicense
        {
            get { return _SelectedLicense; }
            set
            {
                if (_SelectedLicense == value)
                {
                    return;
                }
                _SelectedLicense = value;
                RaisePropertyChanged();
            }
        }

